# Using single quotes
print('Hello world')

# Using double quotes
print("Hello world")

# Using double quotes when the string contains a single quote
print('"Hello, World!", she said.')
# or by escaping
print("\"Hello, World!\", she said.")

# Using double quotes when the string contains a single quote
print("It's a beautiful day!")
# or by escaping
print('It\'s a beautiful day!')